/**
 * Class representing the keyboard input state in the game.
 */
class Keyboard {
    /**
     * Flag indicating whether the left arrow key is pressed.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Flag indicating whether the right arrow key is pressed.
     * @type {boolean}
     */
    RIGHT = false;

    /**
    * Flag indicating whether the up arrow key is pressed.
    * @type {boolean}
    */
    UP = false;

    /**
  * Flag indicating whether the spacebar key is pressed.
  * @type {boolean}
  */
    SPACE = false;

    /**
     * Flag indicating whether the 'D' key is pressed.
     * @type {boolean}
     */
    D = false;
}